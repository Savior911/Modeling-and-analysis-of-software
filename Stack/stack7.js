function longestValidParentheses(s) {
    // Стек для відстеження позицій відкриваючих дужок
    const stack = [-1];
    
    // Змінна для відстеження найдовшої дійсної дужки
    let maxLen = 0;

    // Обхід рядка.
    for (let i = 0; i < s.length; i++) {
        const char = s[i];

        if (char === '(') {
            // Якщо зустрічається відкриваюча дужка, додаємо її позицію до стеку
            stack.push(i);
        } else {
            // Якщо зустрічається закриваюча дужка
            stack.pop(); // Видаляємо позицію відкриваючої дужки зі стеку

            if (stack.length === 0) {
                // Якщо стек порожній, додаємо поточну позицію до стеку для подальших порівнянь
                stack.push(i);
            } else {
                // Інакше розглядаємо можливість оновлення найдовшого дійсного підрядку
                maxLen = Math.max(maxLen, i - stack[stack.length - 1]);
            }
        }
    }

    // Повертаємо найдовшу дійсну дужку
    return maxLen;
}

console.log(longestValidParentheses("(()"));    // Виведе: 2, Пояснення: найдовший дійсні дужки підрядок це "()"
console.log(longestValidParentheses(")()())")); // Виведе: 4, Пояснення: найдовший дійсні дужки підрядок це "()()"
console.log(longestValidParentheses(""));       // Виведе: 0, Пояснення: рядок порожній, довжина найдовших дійсних дужок - 0
