function maxOnes(nums) {
    // Перевіряємо, чи є масив порожнім
    if (nums.length === 0) {
      return 0;
    }
  
    // Зводимо змінну для зберігання поточної кількості повторень 1-ці
    let count = 0;
  
    // Проходимо по масиву
    for (let i = 0; i < nums.length; i++) {
      // Якщо поточний елемент є 1, збільшуємо кількість повторень
      if (nums[i] === 1) {
        count++;
      } 

      else {
        // Якщо поточний елемент є 0, встановлюємо кількість повторень в 1
        count = 1;
      }
  
      // Якщо поточна кількість повторень є більшою за максимальну, зберігаємо її
      if (count > max) {
        max = count;
      }
    }
  
    // Повертаємо максимальну кількість повторень
    return max;
  }
  
  const arr1 = [1, 1, 0, 1, 1, 1];
  const arr2 = [1, 0, 1, 1, 0, 1];

  console.log(maxOnes(arr1)); // 3
  console.log(maxOnes(arr2)); // 2
